//Выполнить деление различных типов(string/boolean, string/number, number/Boolean)

let firstName = "Vitaly"  // string
let isProgrammer = false   // boolean
let age = 21  //number

console.log(firstName / isProgrammer);  //string/boolean
console.log("firstName" / age);           //string/number
console.log(age / isProgrammer);        //number/Boolean